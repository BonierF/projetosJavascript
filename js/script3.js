/*

//Como utilizar o loop for?

Exercício: 



*/


var numero = prompt("Digite um número para descobrir sua tabuada: ")



for (contador = 1; contador <= 10; contador = contador + 1){
    var resultado = numero * contador;
    alert (numero + " x " + contador + " = " + resultado);


}

alert ("Essa é a tabuada do " + numero + " ao 10");